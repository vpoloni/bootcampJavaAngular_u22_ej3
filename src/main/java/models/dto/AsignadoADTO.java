package models.dto;

public class AsignadoADTO {

	// atributos
	private String cientificoAsignado;
	private String proyectoAsignado;

	// setters y getters
	public String getCientificoAsignado() {
		return cientificoAsignado;
	}

	public void setCientificoAsignado(String cientificoAsignado) {
		this.cientificoAsignado = cientificoAsignado;
	}

	public String getProyectoAsignado() {
		return proyectoAsignado;
	}

	public void setProyectoAsignado(String proyectoAsignado) {
		this.proyectoAsignado = proyectoAsignado;
	}

}
